type Status = 'search' | 'choosePath' | 'finish';

export default function HomePage() {
  const [messageApi, contextHolder] = message.useMessage();

  const info = (message: string) => {
    messageApi.info(message);
  };

  const canvasRef = React.useRef<HTMLCanvasElement>(null);
  const [currentState, setCurrentState] = React.useState<Status>('search');
  const [currentPath, setCurrentPath] = React.useState<any>(null);
  const [currentPathIndex, setCurrentPathIndex] = React.useState<number>(0);
  const [inputValue, setInputValue] = React.useState('');

  const onChangeInputValue = React.useCallback(
    (e: React.ChangeEvent<HTMLInputElement>) => {
      setInputValue(e.target.value);
    },
    []
  );

  React.useEffect(() => {
    if (canvasRef.current && currentPath) {
      const canvas = canvasRef.current;
      const ctx = canvas.getContext('2d')!;
      ctx.clearRect(0, 0, 1000, 1000);

      // // 绘制轨迹
      ctx.beginPath();
      currentPath.forEach((path: any) => {
        ctx.moveTo(path.path[0][0], path.path[0][1]);
        path.path.forEach((point: any) => {
          ctx.lineTo(point[0], point[1]);
        });
        ctx.lineWidth = 8;
        ctx.strokeStyle = '#336BFE';
        ctx.stroke();
      });
    }
    if (canvasRef.current && !currentPath) {
      const canvas = canvasRef.current;
      const ctx = canvas.getContext('2d')!;
      ctx.clearRect(0, 0, 1000, 1000);
    }
  }, [canvasRef, currentPath]);
  const showConfirm = () => {
    confirm({
      title: '请确认是否修改行程?',
      icon: <ExclamationCircleFilled />,
      content: '',
      onOk() {
        setCurrentState('search');
        setInputValue('');
        setCurrentPath(null);
        setCurrentPathIndex(0);
      },
      onCancel() {
        console.log('Cancel');
      },
      okButtonProps: {
        className: 'bg-blue-500',
      },
      okText: '我要修改',
      cancelText: '不修改',
    });
  };
  return (
    <Layout>
      {contextHolder}
      {/* <Seo templateTitle='Home' /> */}
      <Seo />

      <main>
        <section className='flex items-center justify-center bg-[#EAEAEA]'>
          <div className=' relative '>
            <Image
              src='/images/map.png'
              alt='map'
              width={1000}
              height={1000}
            ></Image>
            <Image
              src='/images/start.png'
              alt='start'
              width={35}
              height={52}
              className='absolute left-[894px] top-[336px] z-20'
            ></Image>
            {currentPath && (
              <Image
                src='/images/end.png'
                alt='end'
                width={40}
                height={46}
                className='absolute  z-20'
                style={{
                  left:
                    currentPath[0].path[currentPath[0].path.length - 1][0] -
                    20 +
                    'px',
                  top:
                    currentPath[0].path[currentPath[0].path.length - 1][1] -
                    46 +
                    'px',
                }}
              ></Image>
            )}
            {currentState === 'search' && (
              <div className='absolute left-0 right-0 top-10 z-30 flex flex-col items-center justify-center'>
                <div className='flex items-center justify-center'>
                  <input
                    className=' rounded-full bg-white px-4 py-2'
                    value={inputValue}
                    placeholder='请输入您的目的地'
                    onChange={onChangeInputValue}
                  />

                  <button
                    className='ml-10 rounded-full bg-blue-500 px-6 py-2 text-white'
                    onClick={() => {
                      setCurrentState('choosePath');
                    }}
                  >
                    搜索
                  </button>
                </div>
                <div className='mt-6 w-full rounded-lg px-20'>
                  {inputValue !== '' && (
                    <div className=''>
                      {MAPS.filter((map) => map.name.includes(inputValue)).map(
                        (map, mapIdx) => {
                          return (
                            <div
                              className='flex items-center justify-between border-b border-gray-200 bg-white px-4 py-2'
                              key={mapIdx}
                            >
                              <div className='text-gray-500'>{map.name}</div>
                              <button
                                className='rounded-full bg-blue-500 px-6 py-2 text-white'
                                onClick={() => {
                                  setCurrentPath(map.paths);
                                  if (map.paths.length === 1) {
                                    info('暂无备选路线选择');
                                    setCurrentState('finish');
                                  } else {
                                    setCurrentState('choosePath');
                                  }
                                }}
                              >
                                {'到这去>>>'}
                              </button>
                            </div>
                          );
                        }
                      )}
                    </div>
                  )}
                </div>
              </div>
            )}
            <div className='absolute bottom-0 left-0 right-0 top-0 z-10'>
              <canvas
                id='canvas'
                ref={canvasRef}
                width={1000}
                height={1000}
              ></canvas>
            </div>
            {currentState === 'finish' && (
              <div className='absolute bottom-0 left-0 right-0 top-0 z-30 flex w-full flex-col items-center'>
                <div className='border-gray z-30 mt-16 rounded-xl border bg-white px-14 py-6 text-[20px] font-bold '>
                  请确认您的行程信息!
                </div>
                <div className='fixed bottom-20 left-0 right-0 flex items-center justify-center px-20'>
                  <div className='flex w-[1000px] items-center justify-between'>
                    <div className='flex flex-1 items-center justify-center'>
                      <button
                        className=' rounded-full bg-blue-500 px-6 py-2 text-white'
                        onClick={() => {
                          showConfirm();
                        }}
                      >
                        我要修改
                      </button>
                    </div>
                    <div className='flex flex-1 items-center justify-center'>
                      <div className='flex items-center rounded-xl bg-white p-6'>
                        <div className='mr-4'>
                          <div>
                            {'路程: ' +
                              currentPath[currentPathIndex].distance +
                              '米'}
                          </div>
                          <div>
                            {'用时:' +
                              currentPath[currentPathIndex].duration +
                              '分钟'}
                          </div>
                        </div>
                        <Button
                          onClick={() => {
                            info('成功确认');
                          }}
                        >
                          确认上车
                        </Button>
                      </div>
                    </div>
                    <div className='flex-1'></div>
                  </div>
                </div>
              </div>
            )}
          </div>
        </section>
      </main>
    </Layout>
  );
}
